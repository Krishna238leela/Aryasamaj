                    <h4>Havan</h4>
                  <p>Havan is a sacred purifying ritual in Hinduism centered around a fire ceremony, serving as a sacrifice to the fire god Agni. During this ceremony, various offerings such as fruits, honey, or wooden goods are placed into the Havan Kund, the sacrificial fire. It is believed that this act not only purifies the surroundings but also wards off any malevolent spirits, both external and internal, that may be present.</p> 
                  <p>The individual conducting the Havan typically sits facing north, while the head of the household sits facing east in the westward direction. While specific practices may vary, certain customs, like tying kalava around the Lota, filling it with water, and positioning it near the southwest corner of the Havan Kund, are commonly observed. Additional practices, such as tying kalava around a small wooden stick and placing it in a plate along with roli or haldi and chaval, are often incorporated into the ceremony.</p>
				  <p>Overall, the Havan ritual is seen as a potent means of invoking blessings and fostering well-being, happiness, and prosperity within the community. Its significance extends beyond the physical act of sacrifice, encompassing spiritual purification and the fostering of positive energies.</p>
