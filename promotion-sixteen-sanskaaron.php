<div class="fluid-container">
    <div class="row">
    <div class="box col-md-3">
      <h3>GARBHADHAN</h3>
      <p>This Sanskara Is Performed For The Fulfillment Of One's Parental Obligation And A Continuation Of The Human Race. This Is A Fervent Prayer For The Impregnation Of The Foetus With The Life-Giving Soul Force.</p>
    </div>
    <div class="box col-md-3">
      <h3>PUNSAVAN</h3>
      <p>During The Third Or Fourth Month Of Pregnancy The Punsavana (Protection Of The Fetus) Sanskara Is Performed For The Physical Growth Of The Child.</p>
    </div>
    <div class="box col-md-3">
      <h3>SIMANTONNAYAN</h3>
      <p>This Sanskara Is Performed During The Seventh Month Of Pregnancy. The Parents To Be And Their Relatives Offer Prayers To The Almighty Mental Growth Of The Child.</p>
    </div>
    <div class="box col-md-3">
      <h3>JATAKARM</h3>
      <p>The Jatakarma Sanskara (The Child's Nativity) Should Be Done Immediately After The Birth Of The Child. This Sanskara Is Done To Welcome The New-Born Child. The Father Writes AUM On The Tongue Of The Child.</p>
    </div>
    <div class="box col-md-3">
      <h3>NAMAKARAN</h3>
      <p>Nama Literally Means 'Name' And Karan Means 'To Make, To Effect'. Thus, In This Sanskara The Child Is Given A Name. This Sanskara Should Be Performed On The 11th Day After The Birth Of The Child.</p>
    </div>
    <div class="box col-md-3">
      <h3>NISHKRAMAN</h3>
      <p>The 4th Month After The Birth Of The Child, The Nishkramana Sanskara Is Performed. Here The Child Is Taken Outside The Home For The First Time To Be Exposed To The Different Elements Of Nature.</p>
    </div>
    <div class="box col-md-3">
      <h3>ANNA-PRAASHAN</h3>
      <p>Anna Means 'Food' And Prashana Mean 'Eating, Feeding' Thus, Giving Solid Food To The Child For The Very First Time Performs This Sanskara.</p>
    </div>
    <div class="box col-md-3">
      <h3>MUNDAN</h3>
      <p>This Sanskara Can Be Performed Between The Ages Of 1-3 Years Old. In This Sanskara The Hair From The Child's Head Is Shaved Off For The Very First Time.</p>
    </div>
     <div class="box col-md-3">
        <h3>KARNAVEDH</h3>
        <p>The Piercing Of The Lower Lobes Of The Child's Ears At The Age Of Three Performs The Karnavedha Sanskara. Prayers Are Offered To The Almighty For The Physical Well Being Of The Child.</p>
      </div>
      <div class="box col-md-3">
        <h3>UPANAYAN</h3>
        <p>Upanayana Or The Thread Ceremony Is Performed Anytime Between The Ages Of Five To Eight Years Old. Upa Mean 'Approaching Towards' And Nayanam Means 'Leading'. In This Sanskara The Child Is Given The Yajnopavit, Which Is Made From Three Strands.</p>
      </div>
      <div class="box col-md-3">
        <h3>VEDARAMBH</h3>
        <p>This Sanskara Is Done Immediately After The Upanaya Sanskara. Now The Child Becomes A Student. The Child Will Now Gain Knowledge From The Vedas And Other Religious Text As Well The Other Branches Including Mathematics And Science.</p>
      </div>
      <div class="box col-md-3">
        <h3>SAMAVARTAN</h3>
        <p>Between The Ages Of Twenty-One And Twenty-Five Years This Sanskara Is Performed. This Student Should Have Now Completed All His/Her Studies And Start A New Life Of Self-Realization And Independence.</p>
      </div>
      <div class="box col-md-3">
        <h3>VIVAH</h3>
        <p>Vivaha Sanskara Is Performed When The Student Decides To Get Marry. This Is One Of The Most Important Sanskara Among The Sixteen Vedic Sacraments. This Is The Foundation That Forms The Very Basis For A Coordinated Family Life.</p>
      </div>
      <div class="box col-md-3">
        <h3>VAANAPRASTH</h3>
        <p>At Age Fifty-One Years Old, A Person Performs The Vaanaprastha Sanskara By Relinquishing All Livelihood From Which He/She Will Gain Personal Benefits. All The Family Responsibilities Will Be Given To The Children.</p>
      </div>
      <div class="box col-md-3">
        <h3>SANNYAAS</h3>
        <p>This Sanskara Is Performed At The Age Of Seventy-Five Years. In This Sanskara A Person Forsake All Material Things And Starts To Lead A Life Of Meditation And Contemplation Onto The Almighty.</p>
      </div>
      <div class="box col-md-3">
        <h3>ANTYESHTI</h3>
        <p>The Antyeshti Is The Last Sanskara Performed When The Individual Dies. After Death This Sanskara Is Performed When The Body Is Cremated. As The Atma (Soul) Is Immortal, It Cannot Be Destroyed.</p>
      </div>
