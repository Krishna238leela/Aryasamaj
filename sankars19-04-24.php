                                            <p>This ceremony is performed on or after 40 days, but some scriptures allow it at the time of naming ceremony when the child is taken out of the home for the first time. The reason for this Sanskar is to show obedience to the sun, moon, fire, wind etc, -the Panchmahabhut (Five elements) .This is supposed to enhance the age and physical and mental development of the child.</p>
