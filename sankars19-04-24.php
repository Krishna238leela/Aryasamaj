                                            <h5>16. Antyeshti (Last Rite)</h5>
                                          
                                            <p>Antyeshti (literally, last rites), sometimes referred to as Antim-Sanskar, are the rituals associated with funeral. When death is imminent, a small piece of gold, tulsi leaf and drops of Ganga water are put in the mouth of the person on the death bed. The body is laid on the ground with the head towards the north. The eldest son generally performs the last rites before which he takes a purificatory bath amidst the chanting of mantras. The dead body is washed, perfumed and wrapped in a new white cloth and decked with flowers.For ten days following death, food is not prepared at home and relatives and friends take the responsibility of getting food for the family.</p>
