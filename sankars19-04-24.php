                                            <p>This sanskar is performed on sixth month, when the child is given solid food (anna) for the first time. Mantras recited and oblations are offered to the various deities. Sweet porridge or rice pudding can be given to the child, if parents are desirous of nourishment, holy luster, swiftness, or splendor. One of them with curd, honey and ghee is given it to the child while reciting Prasad Mantras.</p>
