                                            <p>This Sanskar is the first time cutting of hair on the child’s head . The ceremony is to be performed on an auspicious day after the age of one year. This ceremony is performed for the development of power better understanding, and for long life. The hair must be disposed of at holy places where no one can find the. Brahmins chant Mantras for a healthy, long life of the child. This Sanskar is restricted to the family level.</p>
