                                            <p>This Sanskar is performed on the tenth , eleventh or twelth day with recitation of Mantras. The baby child gets name on completion of this Sanskar, according to the 27 Nakshatra and the position of the moon at the time of child’s birth. An appropriate name is given to the child according to the planetary position of birth time and the first letter of the name is taken from the Hora Shatra.</p>
