                                            <h5>3. Simantonayana (Satisfying Wishes of the Pregnent Mother)</h5>
                                          <p>This Sanskar is performed during the seventh month of pregnancy and prayers are offered for the healthy physical and mental growth of the child. The other importance of this Sanskar is to free the expectant mother free from worries since the last 3 months are very difficult for pregnant woman- both physically and mentally. A Puja is performed for purification of the atmosphere and as an offering to God for the peace of mother and infant, for giving birth to a peaceful and holy child. This rite is primarily social and festival in nature, intended to keep the pregnant woman in good spirits. A future mother should have good thoughts at all times. She should place Picture of ‘Balgopal’ or ‘Laddu Gopal’ in her home. She should read the Gita and other scriptures in addition to performing her daily work and should avoid thrilling books and movies. During Solar and lunar eclipses, a woman should not use any kind of weapons. During normal times, she should avoid violent thoughts. Her husband should help keep her peaceful and cheerful.</p>
