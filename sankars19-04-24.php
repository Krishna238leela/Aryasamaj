                                            <h5>14. Vanprastha (Preparing for Renouncing)</h5>
                                          
                                            <p>This ceremony is performed at the age of 50, in some cases at the age of 60. With the commencement of his ceremony, a man completes his Grahastha Dharma and enters into Vanprastha Ashram (forest hermit). Man withdraws himself from all worldly activities, retires into the forest and prepares himself for taking sanyas. This is the life of a Vanprastha.</p>
